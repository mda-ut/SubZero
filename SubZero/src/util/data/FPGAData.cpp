#include "FPGAData.h"

/* ==========================================================================
 * CONSTRUCTOR & DESTRUCTOR
 * ==========================================================================
 */

FPGAData::FPGAData(std::string dataID, double depth, double roll, double heading) {
	this->dataID = dataID;
	this->depth = depth;
	this->roll = roll;
	this->heading = heading;
}

FPGAData::~FPGAData() {
}

/* ==========================================================================
 * FRIEND FUNCS
 * ==========================================================================
 */

void FPGAData::setDepth(double newDepth) {
	this->depth = newDepth;
}

void FPGAData::setRoll(double newRoll) {
	this->roll = newRoll;
}

void FPGAData::setHeading(double newHeading) {
	this->heading = newHeading;
}

/* ==========================================================================
 * FPGA MANIPULATION FUNCS
 * ==========================================================================
 */

double FPGAData::getDepth() {
	return this->depth;
}

double FPGAData::getRoll() {
	return this->roll;
}

double FPGAData::getHeading() {
	return this->heading;
}

/* ==========================================================================
 * OPERATOR OVERLOAD
 * ==========================================================================
 */

FPGAData& FPGAData::operator =(FPGAData& rhs) {
	return new FPGAData(rhs);
}

FPGAData::FPGAData(const FPGAData & obj) {
	this->dataID = obj.dataID;
	this->msg = obj.msg;
	this->depth = obj.depth;
	this->roll = obj.roll;
	this->heading = obj.heading;
}
